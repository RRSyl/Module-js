exports.calculateSquareArea = (side) =>{
    return side*side;
}

exports.calculateSquarePerimeter = (side) =>{
    return side*4;
}

exports.calculateRectangleArea = (width,height) =>{
    return width*height;
}

exports.calculateRectanglePerimeter = (width,height) =>{
    return 2*(width+height);
}
